
/**
 * @desc Get sum of two numbers
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number} Sum of number 1 and number 2
 */
const getSum = (num1, num2) => {
  return num1 + num2
}

/**
 * @desc Get division of number1 / nmber2
 * @param {Number} num1
 * @param {Number} num2
 * @returns {Number}
 */
const getDivision = (num1, num2) => {
  return num1 / num2
}

module.exports = {
  getSum,
  getDivision
}
